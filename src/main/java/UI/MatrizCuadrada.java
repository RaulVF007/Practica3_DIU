    private void maxValue1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maxValue1ActionPerformed
        if((Integer.parseInt(maxValue1.getText()) < 0 || Integer.parseInt(maxValue1.getText()) > 100)){
            JOptionPane.showMessageDialog(null, "Sólo se permiten valores entre 0 y 100", "ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
            maxValue = Integer.parseInt(maxValue1.getText());
            initiateMatriz();
        }
    }//GEN-LAST:event_maxValue1ActionPerformed

