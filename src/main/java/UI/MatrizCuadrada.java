    private void initiateMatriz() {
        String items="";
        for (int i = 0; i < matriz.length; i++) {
            for (int j = 0; j < matriz.length; j++) {
                matriz[i][j] = new Random().nextInt(maxValue+1 - minValue) + minValue;
                items += matriz[i][j] + "\t";
            }
            items += "\n";
        }
        areaTexto.setText(items);
    }
